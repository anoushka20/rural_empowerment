																  <h4>स्वतंत्रता का अधिकार</h4>
																   <div id="collapse2" class="collapse">
																<p>संविधान के निर्माताओं द्वारा महत्वपूर्ण माने गए व्यक्तिगत अधिकारों की गारंटी देने की दृष्टि से स्वतंत्रता के अधिकार को अनुच्छेद 19-22 में शामिल किया गया है और इन अनुच्छेदों में कुछ प्रतिबंध भी शामिल हैं जिन्हें विशेष परिस्थितियों में राज्य द्वारा व्यक्तिगं स्वतंत्रता पर लागू किया जा सकता है। अनुच्छेद 19 नागरिक अधिकारों के रूप में छः प्रकार की स्वतंत्रताओं की गारंटी देता है जो केवल भारतीय नागरिकों को ही उपलब्ध हैं। इनमें शामिल हैं(19 अ ) भाषण और अभिव्यक्ति की स्वतंत्रता, (19 ब ) शांतिपूर्वक बिना हथियारों के एकत्रित होने और सभा करने की स्वतंत्रता , भारत के राज्यक्षेत्र में कहीं भी आने-जाने की स्वतंत्रतता, भारत के किसी भी भाग में बसने और निवास करने की स्वतंत्रता तथा कोई भी पेशा अपनाने की स्वतंत्रता। ये सभी स्वतंत्रताएं अनुच्छेद 19 में ही वर्णित कुछ उचित प्रतिबंधों के अधीन होती हैं, दिन्हें राज्य द्वारा उन पर लागू किया जा सकता है। किस स्वतंत्रता को प्रतिबंधित किया जाना प्रस्तावित है, इसके आधार पर प्रतिबंधों को लागू करने के आधार बदलते रहते हैं, इनमें शामिल हैं राष्ट्रीय सुरक्षा, सार्वजनिक व्यवस्था, शालीनता और नैतिकता, न्यायालय की अवमानना, अपराधों को भड़काना और मानहानि। आम जनता के हित में किसी व्यापार, उद्योग या सेवा का नागरिकों के अपवर्जन के लिए राष्ट्रीयकरण करने के लिए राज्य को भी सशक्त किया गया है।
															
																	अनुच्छेद 19 द्वारा गारंटीशुदा स्वतंत्रताओं की आगे अनुच्छेद 20-22 द्वारा रक्षा की जाती है। इन अनुच्छेदों के विस्तार, विशेष रूप से निर्धारित प्रक्रिया के सिद्धांत के संबंध में, पर संविधान सभा में भारी बहस हुई थी। विशेष रूप से बेनेगल नरसिंह राव ने यह तर्क दिया कि ऐसे प्रावधान को लागू होने से सामाजिक कानूनों में बाधा आएगी तथा व्यवस्था बनाए रखने में प्रक्रियात्मक कठिनाइयां उत्पन्न होंगी, इसलिए इसे पूरी तरह संविधान से बाहर ही रखा जाए। संविधान सभा ने 1948 में अंततः "निर्धारित प्रक्रिया" शब्दों को हटा दिया और उनके स्थान पर “कानून द्वारा स्थापित प्रक्रिया” को शामिल कर लिया। परिणाम के रूप में एक, अनुच्छेद 21,यह जापान से लिया गया है। जो विधि द्वारा स्थापित प्रक्रिया के अनुसार होने वाली कार्यवाही को छोड़ कर, जीवन या व्यक्तिगत संवतंत्रता में राज्य के अतिक्रमण से बचाता है, के अर्थ को 1978 तक कार्यकारी कार्यवाही तक सीमित समझा गया था। हालांकि, 1978 में, मेनका गांधी बनाम भारत संघ के मामले में सर्वोच्च न्यायालय ने अनुच्छेद 21 के संरक्षण को विधाई कार्यवाही तक बढ़ाते हुए निर्णय दिया कि किसी प्रक्रिया को निर्धारित करने वाला कानून उचित, निष्पक्ष और तर्कसंगत होना चाहिए, और अनुच्छेद 21 में निर्धारित प्रक्रिया को प्रभावी ढंग से पढ़ा। इसी मामले में सुप्रीम कोर्ट ने यह भी कहा कि अनुच्छेद 21 के अंतर्गत "जीवन" का अर्थ मात्र एक "जीव के अस्तित्व" से कहीं अधिक है; इसमें मानवीय गरिमा के साथ जीने का अधिकार तथा वे सब पहलू जो जीवन को "अर्थपूर्ण, पूर्ण तथा जीने योग्य" बनाते हैं, शामिल हैं।[50] इस के बाद की न्यायिक व्याख्याओं ने अनुच्छेद 21 के अंदर अनेक अधिकारों को शामिल करते हुए इसकी सीमा का विस्तार किया है जिनमें शामिल हैं आजीविका, स्वच्छ पर्यावरण, अच्छा स्वास्थ्य, अदालतों में तेवरित सुनवाई तथा कैद में मानवीय व्यवहार से संबंधित अधिकार।  प्राथमिक स्तर पर शिक्षा के अधिकार को 2002 के 86वें संवैधानिक संशोधन द्वारा अनुच्छेद 21ए में मूल अधिकार बनाया गया है।
															
																	अनुच्छेद 20 अपराधों के लिए दोषसिद्धि के संबंध में संरक्षण प्रदान करता है, जिनमें शामिल हैं पूर्वव्यापी कानून व दोहरे दंड के विरुद्ध अधिकार तथा आत्म-दोषारोपण से स्वतंत्रता प्रदान करता है। अनुच्छेद 22 गिरफ्तार हुए और हिरासत में लिए गए लोगों को विशेष अधिकार प्रदान करता है, विशेष रूप से गिरफ्तारी के आधार सूचित किए जाने, अपनी पसंद के एक वकील से सलाह करने, गिरफ्तारी के 24 घंटे के अंदर एक मजिस्ट्रेट के समक्ष पेश किए जाने और मजिस्ट्रेट के आदेश के बिना उस अवधि से अधिक हिरासत में न रखे जाने का अधिकार। संविधान राज्य को भी अनुच्छेद 22 में उपलब्ध रक्षक उपायों के अधीन, निवारक निरोध के लिए कानून बनाने के लिए अधिकृत करता है। निवारक निरोध से संबंधित प्रावधानों पर संशयवाद तथा आशंकाओं के साथ चर्चा करने के बाद संविधान सभा ने कुछ संशोधनों के साथ 1949 में अनिच्छा के साथ अनुमोदन किया था। अनुच्छेद 22 में प्रावधान है कि जब एक व्यक्ति को निवारक निरोध के किसी भी कानून के तहत हिरासत में लिया गया है, ऐसे व्यक्ति को राज्य केवल तीन महीने के लिए परीक्षण के बिना गिरफ्तार कर सकता है, इससे लंबी अवधि के लिए किसी भी निरोध के लिए एक सलाहकार बोर्ड द्वारा अधिकृत किया जाना आवश्यक है। हिरासत में लिए गए व्यक्ति को भी अधिकार है कि उसे हिरासत के आधार के बारे में सूचित किया जाएगा और इसके विरुद्ध जितना जल्दी अवसर मिले अभ्यावेदन करने की अनुमति दी जाएगी।
