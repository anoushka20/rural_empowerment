																	  <h4>समानता का अधिकार</h4>
																		 <div id="collapse1" class="collapse">
																			<p>	 
																	यह अनुच्छेद 14-18 में सन्निहित हैं जिसमें सामूहिक रूप से कानून के समक्ष समानता तथा गैर-भेदभाव के सामान्य सिद्धांत शामिल हैं, तथा अनुच्छेद 17-18 जो सामूहिक रूप से सामाजिक समानता के दर्शन को आगे बढ़ाते हैं। 
																	अनुच्छेद 14 कानून के समक्ष समानता की गारंटी देता है, इसके साथ ही भारत की सीमाओं के अंदर सभी व्यक्तियों को कानून का समान संरक्षण प्रदान करता है। 
																	इस में कानून के प्राधिकार की अधीनता सबके लिए समान है, साथ ही समान परिस्थितियों में सबके साथ समान व्यवहार। 
																	उत्तरवर्ती में राज्य वैध प्रयोजनों के लिए व्यक्तियों का वर्गीकरण कर सकता है, बशर्ते इसके लिए यथोचित आधार मौजूद हो, जिसका अर्थ है कि वर्गीकरण मनमाना न हो, वर्गीकरण किये जाने वाले लोगों में सुगम विभेदन की एक विधि पर आधारित हो, 
																	साथ ही वर्गीकरण के द्वारा प्राप्त किए जाने वाले प्रयोजन का तर्कसंगत संबंध होना आवश्यक है।
																	
																	अनुच्छेद 15 केवल धर्म, मूलवंश, जाति, लिंग, जन्म स्थान, या इनमें से किसी के ही आधार पर भेदभाव पर रोक लगाता है। 
																	अंशतः या पूर्णतः राज्य के कोष से संचालित सार्वजनिक मनोरंजन स्थलों या सार्वजनिक रिसोर्ट में निशुल्क प्रवेश के संबंध में यह अधिकार राज्य के साथ-साथ निजी व्यक्तियों के खिलाफ भी प्रवर्तनीय है। 
																	हालांकि, राज्य को महिलाओं और बच्चों या अनुसूचित जाति व अनुसूचित जनजाति सहित सामाजिक और शैक्षिक रूप से पिछड़े वर्गों के नागरिकों के लिए विशेष प्रावधान बनाने से राज्य को रोका नहीं गया है। 
																	इस अपवाद का प्रावधान इसलिए किया गया है क्योंकि इसमें वर्णित वर्गो के लोग वंचित माने जाते हैं और उनको विशेष संरक्षण की आवस्यकता है। 
																	अनुच्छेद 16 सार्वजनिक रोजगार के संबंध में अवसर की समानता की गारंटी देता है और राज्य को किसी के भी खिलाफ केवल धर्म, नस्ल, जाति, लिंग, वंश, जन्म स्थान या इनमें से किसी एक के आधार पर भेदभाव करने से रोकता है।
																	 किसी भी पिछड़े वर्ग के नागरिकों का सार्वजनिक सेवाओं में पर्याप्त प्रतिनिधित्व सुनुश्चित करने के लिए उनके लाभार्थ सकारात्मक कार्रवाई के उपायों के कार्यान्वयन हेतु अपवाद बनाए जाते हैं, साथ ही किसी धार्मिक संस्थान के एक 
																	 पद को उस धर्म का अनुसरण करने वाले व्यक्ति के लिए आरक्षित किया जाता है।
																	
																	अस्पृश्यता की प्रथा को अनुच्छेद 17 के अंतर्गत एक दंडनीय अपराध घोषित कर किया गया है, इस उद्देश्य को आगे बढ़ाते हुए नागरिक अधिकार संरक्षण अधिनियम 1955 संसद द्वारा अधिनियमित किया गया है। अनुच्छेद 18 राज्य को सैन्य या शैक्षणिक विशिष्टता को छोड़कर किसी को भी कोई पदवी दे्ने से रोकता है तथा कोई भी भारतीय नागरिक किसी विदेशी राज्य से कोई पदवी स्वीकार नहीं कर सकता। इस प्रकार, भारतीय कुलीन उपाधियों और अंग्रेजों द्वारा प्रदान की गई और अभिजात्य उपाधियों को समाप्त कर दिया गया है। हालांकि, भारत रत्न पुरस्कारों जैसे, भारतरत्न को सर्वोच्च न्यायालय द्वारा इस आधार पर मान्य घोषित किया गया है कि ये पुरस्कार मात्र अलंकरण हैं और प्रप्तकर्ता द्वारा पदवी के रूप में इस्तेमाल नहीं किया जा सकता।
